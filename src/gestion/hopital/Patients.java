            private void AfficherPatients(){
        try{
            
       DefaultTableModel model = (DefaultTableModel) PatientsTable.getModel();
      model.setRowCount(0);
       Connection connection = DriverManager.getConnection("jdbc:derby://localhost:1527/HospitalJavaDB","User1","root");

       String query = "SELECT * FROM User1.PATIENTTABLE"; 
       Statement cmd = connection.createStatement();
       ResultSet rs = cmd.executeQuery(query);

       while (rs.next()) {
                String PatId = rs.getString("PatID");
                String PatName = rs.getString("PatName");
                String PatAd = rs.getString("PatAd");
                String PatPhone = rs.getString("Patphone");
                String PatAge = rs.getString("PatAge");
                String PatGen = rs.getString("PatGen");
                String PatGrp = rs.getString("PatGrp");
                String Pathologies = rs.getString("Pathologies");

                model.addRow(new Object[]{PatId, PatName,PatAd ,PatPhone ,PatAge, PatGen,PatGrp,Pathologies});
            }

            connection.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

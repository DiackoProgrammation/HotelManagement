    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        if(PatIdBox.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Veuillez selectionner l'ID à supprimé");
        }try{
            Connection connection = DriverManager.getConnection("jdbc:derby://localhost:1527/HospitalJavaDB","User1","root");
            String query = "DELETE FROM PATIENTTABLE WHERE PATID=?";
            PreparedStatement cmd = connection.prepareStatement(query);
            cmd.setString(1,PatIdBox.getText());
            cmd.executeUpdate();
            JOptionPane.showMessageDialog(this, "Patient supprimé");
            connection.close();
            
                          
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_DeleteBtnActionPerformed

    
    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
           if(PatIdBox.getText().isEmpty()|| PatAdBox.getText().isEmpty() || PatAgeBox.getText().isEmpty() || PatNameBox.getText().isEmpty() ||
            PatPhoneBox.getText().isEmpty() || PathologieBox.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Informations manquantes");

        }else{
               
        try{
           DefaultTableModel model = (DefaultTableModel) PatientsTable.getModel();
              Connection connection = DriverManager.getConnection("jdbc:derby://localhost:1527/HospitalJavaDB","User1","root");
              String query = "UPDATE PATIENTTABLE SET PATNAME=?, PATAD=?, PATPHONE=? ,PATAGE=?, PATGEN=?, PATGRP=?, PATHOLOGIES=? WHERE PATID=?";            
              PreparedStatement cmd = connection.prepareStatement(query);
              cmd.setString(1,PatNameBox.getText());
               cmd.setString(2,  PatAdBox.getText());
               cmd.setString(3, PatPhoneBox.getText());
               cmd.setString(4, PatAgeBox.getText());
               cmd.setString(5, PatGenreCBox.getSelectedItem().toString());
               cmd.setString(6,PatGrpCBox.getSelectedItem().toString());
               cmd.setString(7,PathologieBox.getText());
               cmd.setString(8, PatIdBox.getText());
               cmd.executeUpdate();
               JOptionPane.showMessageDialog(this, "Patient modifié avec succès");
               connection.close();
               AfficherPatients();
               }catch(Exception ex){
ex.printStackTrace();
}
           } 
    }//GEN-LAST:event_UpdateBtnActionPerformed

    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        if(PatIdBox.getText().isEmpty()|| PatAdBox.getText().isEmpty() || PatAgeBox.getText().isEmpty() || PatNameBox.getText().isEmpty() ||
            PatPhoneBox.getText().isEmpty() || PathologieBox.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Informations manquantes");

        }else{
        try{
           
                  Connection connection = DriverManager.getConnection("jdbc:derby://localhost:1527/HospitalJavaDB","User1","root");

           String query = "INSERT INTO PATIENTTABLE VALUES(?,?,?,?,?,?,?,?)";
           PreparedStatement cmd = connection.prepareStatement(query);
           cmd.setInt(1, Integer.valueOf(PatIdBox.getText()));
           cmd.setString(2, PatNameBox.getText());
           cmd.setString(3,PatAdBox.getText());
           cmd.setInt(4, Integer.valueOf(PatPhoneBox.getText()));
           cmd.setString(5, PatAgeBox.getText());
           cmd.setString(6, PatGenreCBox.getSelectedItem().toString());
           cmd.setString(7, PatGrpCBox.getSelectedItem().toString());
           cmd.setString(8, PathologieBox.getText());
           cmd.executeUpdate();
           JOptionPane.showMessageDialog(this, "Patient inséré avec succès");
           connection.close();
           AfficherPatients();

        }catch(Exception ex){
            ex.printStackTrace();
        }
        }
    }//GEN-LAST:event_AddBtnActionPerformed

